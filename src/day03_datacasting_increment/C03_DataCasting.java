package day03_datacasting_increment;

public class C03_DataCasting {

	public static void main(String[] args) {
		
		
		byte sayi1= 21;
		
		System.out.println("byte degisken ile :" +sayi1);

		short sayi2 = sayi1;
		
		System.out.println("short degisken ile :"+sayi2);
		
		int sayi3=sayi2;
		
		System.out.println("int deggisken ile : "+sayi3);
		
		float sayi4 = sayi3;
		
		System.out.println("float degiskeni ile : "+sayi4);
		
		double sayi5=sayi4;
		
		System.out.println("double degiskeni ile :"+ sayi5);
		
		
				

	}

}
